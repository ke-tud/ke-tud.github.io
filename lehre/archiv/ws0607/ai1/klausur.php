<a name="pschl"></a>
<h1>Pr&uuml;fungs- und Studienleistung</h1>
<blockquote>
Je nach Ihrem Studiengang m&uuml;ssen Sie eine Leistung in Allgemeine
Informatik 1 pflichtweise erbringen oder k&ouml;nnen Allgemeine Informatik 1
als Wahlpflichtveranstaltung belegen (Wenn beides nicht zutrifft,
belegen Sie Allgemeine Informatik 1 als freiwillige Zusatzleistung oder
als Gasth&ouml;rer). Je nach Studiengang ist die Leistung entweder eine
sogenannte &quot;Pr&uuml;fungsleistung&quot; oder eine sogenannte &quot;Studienleistung&quot;.
Der Unterschied besteht im wesentlichen darin, dass die Regeln f&uuml;r
Pr&uuml;fungsleistungen rigider sind: Sie m&uuml;ssen sich beim zust&auml;ndigen
Pr&uuml;fungssekretariat anmelden; sie m&uuml;ssen sich ggf. auch dort wieder
rechtzeitig abmelden, wenn Sie die Pr&uuml;fung doch nicht absolvieren
wollen; Sie d&uuml;rfen eine Pr&uuml;fung nur eine begrenzte Anzahl von Malen
nicht bestehen; wenn Sie bestanden haben, gibt es keine Wiederholung zur
Notenverbesserung mehr (au&szlig;er bei einem &quot;Freischuss&quot;). Bei einer
Studienleistung (h&auml;ufig auch &quot;&Uuml;bungsschein&quot; oder &quot;Schein&quot; genannt)
m&uuml;ssen Sie sich nur bei uns an- und ggf. wieder abmelden, und Sie k&ouml;nnen
eine Studienleistung beliebig h&auml;ufig wiederholen, auch zur
Notenverbesserung - falls Sie entsprechend lange an der Uni bleiben und
dann irgendwann kr&auml;ftig Studiengeb&uuml;hren zahlen wollen.<br>
<br>
F&uuml;r eine Studien- oder Pr&uuml;fungsleistung m&uuml;ssen Sie exakt dieselben
Leistungen erbringen, siehe unten, Stichwort <a href="#leist">&quot;Zu erbringende
Leistungen&quot;</a>. F&uuml;r die Frage, ob Sie eine Pr&uuml;fungs- oder Studienleistung
ben&ouml;tigen, haben wir ohne Garantie einen unverbindlichen Leitfaden zusammengestellt,
siehe unten, Stichwort <a href="#welche">&quot;Wer muss welche Art von Leistung erwerben?&quot;</a>.
</blockquote>
