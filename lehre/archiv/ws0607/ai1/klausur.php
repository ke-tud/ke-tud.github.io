<a name="regeln"></a>
<h1>Regeln</h1>
<blockquote>
<b>Beachten Sie bitte die folgenden Hinweise zur Klausurbearbeitung,
die auch noch einmal auf dem Deckblatt der Klausur stehen werden:</b><br>
<ul>
  <li>
    Schreiben Sie Ihre Matrikelnummer zu Ihrer eigenen Sicherheit auf jedes einzelne
    Abgabeblatt oben hinter &quot;Ihre Matrikelnummer:&quot;.
  <br><br></li>
  <li>
    Wenn Sie ein Problem haben (Papier fehlt, Stift versagt, Toilettendruck, Panik beim
    Durchlesen der Aufgaben......), heben Sie den Arm. Wir werden uns sofort um Sie k&uuml;mmern.
  <br><br></li>
  <li>
    Wenn Sie sich nicht ganz sicher sind, wie eine Aufgabe gemeint ist, oder wenn
    Sie sich nicht ganz sicher sind, ob Sie ein Wort oder einen Satz richtig verstanden
    haben, heben Sie ebenfalls den Arm. Wir werden Ihnen die Aufgabe sofort noch einmal
    erkl&auml;ren.<br>
    Gerade wenn Deutsch nicht Ihre Muttersprache ist, sollten Sie lieber
    einmal zuviel als einmal zuwenig fragen.
  <br><br></li>
  <li>
    Sie d&uuml;rfen weder Bleistift noch Rot- oder Gr&uuml;nstift verwenden
    (auch nicht f&uuml;r Zeichnungen oder &quot;Herumprobieren&quot;!).
  <br><br></li>
  <li>
    Lassen Sie die Antwortbl&auml;tter unbedingt zusammengeheftet.<br>
    Falls Ihre Bl&auml;tter auseinandergerissen sind, heben Sie den Arm.
    Wir werden sie dann wieder f&uuml;r Sie zusammenheften.
  <br><br></li>
  <li>
    Schreiben Sie Ihre L&ouml;sung zu jeder Aufgabe unbedingt nur und
    ausschlie&szlig;lich auf das Antwortblatt zu dieser Aufgabe.<br>
    Sie m&uuml;ssen davon ausgehen, dass L&ouml;sungen,
    die auf einem falschen Blatt stehen, nicht bewertet werden.
  <br><br></li>
  <li>
    Sie bekommen zus&auml;tzlich lose &quot;Schmierbl&auml;tter&quot;, die Sie zum Ausprobieren
    verwenden k&ouml;nnen. Was auf diesen Bl&auml;ttern steht, wird generell nicht bewertet
    (auch nicht, wenn Sie von anderswoher auf die Schmierbl&auml;tter verweisen).
  <br><br></li>
  <li>
    Beantworten Sie genau die Fragen, die gestellt werden, nicht mehr und nicht weniger.
    Wenn Sie zur Beantwortung einer Frage weiter ausholen und etwas Allgemeineres zum Thema
    schreiben wollen, machen Sie den Bezug zur Aufgabenstellung unmissverst&auml;ndlich klar.
  <br><br></li>
  <li>
    Bem&uuml;hen Sie sich um knappe und pr&auml;zise Ausdrucksweise (unter Verwendung der
    daf&uuml;r in der Vorlesung eingef&uuml;hrten Begriffe) und um eine m&ouml;glichst
    systematische, &uuml;bersichtliche Darstellung mit sauberem Schriftbild.<br>
    Sie m&uuml;ssen davon ausgehen, dass eine unsystematische, un&uuml;bersichtliche
    Darstellung oder das &quot;Schreiben von Romanen&quot; beim Korrigieren als ein Zeichen
    gewertet wird, dass Ihr Verst&auml;ndnis des Stoffes unsystematisch geblieben ist.
  <br><br></li>
  <li>
    Beachten Sie auch, dass der Text einer Aufgabe oder Teilaufgabe Ihnen mehrere Teilaufgaben
    aufgeben kann. Gehen Sie jeden Aufgabentext Satz f&uuml;r Satz genau durch, um nichts
    zu &uuml;bersehen. Jedes einzelne Wort kann wichtig sein.
  <br><br></li>
  <li>
    Denken Sie daran, dass es nicht ausreicht, wenn Ihre L&ouml;sung korrekt ist;
    sie muss auch <i>nachvollziehbar</i> sein.<br>
    Bem&uuml;hen Sie sich insbesondere, &quot;Basteleien&quot; auf die Schmierbl&auml;tter
    zu beschr&auml;nken und auf den eigentlichen Aufgabenbl&auml;ttern m&ouml;glichst nur
    eine endg&uuml;ltige Reinschrift zu erstellen.
  <br><br></li>
  <li>
    Handys und andere elektronische Ger&auml;te: <b>aus, aus, aus</b>!
  <br></li>
</ul>
<b>Achtung:</b>
Wenn Sie statt mit uns mit Kommilitonen sprechen, kann das als
Betrugsversuch gewertet und entsprechend behandelt werden.
</blockquote>
