<a name="welche"></a>
<h1>Wer muss welche Art von Leistung erwerben?</h1>
<blockquote>
<b>Grundregel: Verbindlich Auskunft geben kann Ihnen dazu  immer nur Ihr
Pr&uuml;fungssekretariat bzw. die Studienberatung Ihres Fachbereichs!</b><br>
<br>
Wir sind nur &quot;ausf&uuml;hrende Kr&auml;fte&quot;, d.h. jeder <i>kann</i>
bei uns eine Studien- oder Pr&uuml;fungsleistung erwerben. Ob er die braucht
oder nicht, muss er mit seinem Fachbereich abkl&auml;ren. Wir haben zwar im
Laufe der Zeit Erfahrungen gesammelt, wer was braucht, aber das ist
nat&uuml;rlich nicht zu verstehen als eine zeitlos verbindliche Antwort.
Trotzdem haben wir diese Erfahrungen hier zusammengestellt, um Ihnen einen
ersten Anhaltspunkt zu geben:<br>
<ul>
  <li>
    <b>Studierende aus dem Fachbereich Elektrotechnik (ETIT), neuere Studienordnung (ab WS 01/02)</b><br>
    Pr&uuml;fungsleistung
  <br><br></li>
  <li>
    <b>Wirtschaftsingenieurwesen mit technischer Fachrichtung Elektrotechnik (WI-ET)</b><br>
    Studienleistung
  <br><br></li>
  <li>
    <b>Sportwissenschaften mit Schwerpunkt Informatik</b><br>
    Studienleistung
  <br><br></li>
  <li>
    <b>Physik</b><br>
    Pr&uuml;fungsleistung
  <br><br></li>
  <li>
    <b>Magister</b><br>
    In diesem Bereich verf&uuml;gen wir &uuml;ber recht wenige Erfahrungen.
    Unserer Ansicht nach ben&ouml;tigen diese Studenten eine Studienleistung.
  <br><br></li>
  <li>
    <b>Lehramt berufliche Schulen, alte Prüfungsordnung (vor WS 05/06)</b><br>
    Studienleistung
  <br><br></li>
  <li>
    <b>Geod&auml;sie</b><br>
    Nach unseren Informationen eine Studienleistung.
  <br></li>
</ul>
<b>Diese Angaben sind ohne Gew&auml;hr und es gilt die oben genannte
Grundregel: Verbindlich Auskunft geben kann Ihnen immer nur der
Pr&uuml;fungsausschuss oder die Studienberatung f&uuml;r Ihren Studiengang.</b><br>
Sollten Sie bei Ihren Recherchen von unseren Angaben abweichende
Ergebnisse erzielen, bitten wir Sie, uns diese umgehend mitzuteilen.
So k&ouml;nnen wir diese Informationen im Interesse aller anderen
Studenten hier einpflegen.<br>
<br>
F&uuml;r die Klausur m&uuml;ssen Sie sich beim zentralen Studiensekretariat im alten
Hauptgeb&auml;ude (S1/03) anmelden. Die n&ouml;tigen Formulare gibt es auch online auf
<a href="http://www.tu-darmstadt.de/pvw/abt_i/ref_ib/pruefsek/anmeldeformulare.tud" target="_blank">
http://www.tu-darmstadt.de/pvw/abt_i/ref_ib/pruefsek/anmeldeformulare.tud</a>.
</blockquote>
