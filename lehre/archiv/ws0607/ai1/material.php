<h1>KarelJ</h1>
<blockquote>
Als Vorstufe zum &quot;richtigen&quot; Java werden wir in der Allgemeinen Informatik 1
KarelJ benutzen, eine Art vereinfachtes Java mit dem netten Roboter Karel J. Robot,
der in seiner rechteckigen Welt so einiges machen kann.<br>
<br>
Eine Entwicklungsumgebung ist auf den Poolrechner installiert, zum Starten einfach
&quot;KarelJIDE&quot; eingeben (Gro&szlig;-/Kleinschreibung!). Sie k&ouml;nnen die
KarelJIDE aber auch <a href="#wilinks">hier</a> herunterladen. Dabei m&uuml;ssen Sie
aber beachten, dass die KarelJIDE nicht mit Java 1.5.x funktioniert, sondern nur mit
Java 1.4.x oder Java SE 6 (Java 1.6)! In der ZIP-Datei finden Sie eine Batch-Datei namens KarelJIDE,
die Sie ausf&uuml;hren m&uuml;ssen, um die KarelJIDE zu starten.
<br>
</blockquote>
